
public class KaryawanClone {
	private String kodeClone;
	private String namaClone;
	private String jenisKelaminClone;
	private String jabatanClone;
	private int gajiClone;
	
	public KaryawanClone(String kodeClone, String namaClone, String jenisKelaminClone, String jabatanClone,
			int gajiClone) {
		super();
		this.kodeClone = kodeClone;
		this.namaClone = namaClone;
		this.jenisKelaminClone = jenisKelaminClone;
		this.jabatanClone = jabatanClone;
		this.gajiClone = gajiClone;
	}
	
	public String getKodeClone() {
		return kodeClone;
	}
	public void setKodeClone(String kodeClone) {
		this.kodeClone = kodeClone;
	}
	public String getNamaClone() {
		return namaClone;
	}
	public void setNamaClone(String namaClone) {
		this.namaClone = namaClone;
	}
	public String getJenisKelaminClone() {
		return jenisKelaminClone;
	}
	public void setJenisKelaminClone(String jenisKelaminClone) {
		this.jenisKelaminClone = jenisKelaminClone;
	}
	public String getJabatanClone() {
		return jabatanClone;
	}
	public void setJabatanClone(String jabatanClone) {
		this.jabatanClone = jabatanClone;
	}
	public int getGajiClone() {
		return gajiClone;
	}
	public void setGajiClone(int gajiClone) {
		this.gajiClone = gajiClone;
	}
	
	
	
	
}
